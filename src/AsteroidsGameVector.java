
public class AsteroidsGameVector {

	private float x;
	private float y;
	
	public AsteroidsGameVector(float x, float y){
		this.x = x;
		this.y = y;
	}
	
	public void setXY(float x, float y){
		this.x = x;
		this.y = y;
	}
	
	public float getX(){
		return x;
	}
	
	public float getY(){
		return y;
	}
	
}
